vector<int> z_function(string s) {
    int n = (int) s.length();
    vector<int> z(n);
    for (int i = 1, l = 0, r = 0; i < n; ++i) {
        if (i <= r)
            z[i] = min(r - i + 1, z[i - l]);
        while (i + z[i] < n && s[z[i]] == s[i + z[i]])
            ++z[i];
        if (i + z[i] - 1 > r)
            l = i, r = i + z[i] - 1;
    }
    return z;
}

void solve() {
    int n;
    cin >> n;

    string s;
    cin >> s;

    n = s.size();
    int ans = 0;
    for (int i = 0; i < n; i++) {
        vector<int> v = z_function(s.substr(i));
        for (int i = 0; i < v.size(); i++) {
            if (v[i] > i)continue;
            ans = max(ans, v[i]);
        }
    }
    cout << ans << endl;
}

